/**
 * add - sum
 * @a: first num
 * @b: sec num
 * Return: result
 */
int add(int a, int b)
{
      return (a + b);
}
/**
 * sub - add
 * @a: first num
 * @b: sec num
 * Return: result
 */
int sub(int a, int b)
{
      return (a - b);
}
/**
 * mul - addition
 * @a: first num
 * @b: sec num
 * Return: result
 */
int mul(int a, int b)
{
      return (a * b);
}
/**
 * div - add
 * @a: first num
 * @b: sec num
 * Return: result
 */
int div(int a, int b)
{
      return (a / b);
}
/**
 * mod - addition
 * @a: 1st num
 * @b: 2ed num
 * Return: result
 */
int mod(int a, int b)
{
      return (a % b);
}
